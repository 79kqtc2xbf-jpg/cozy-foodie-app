(function applyManualMoreSteps(){
  if (window.CF19_MANUAL_MORE_STEPS_APPLIED) return;
  window.CF19_MANUAL_MORE_STEPS_APPLIED = true;
  window.CF19_VERSION = "webstable19-manual-more-steps";

  function withManualSteps(recipe) {
    if (!recipe || !recipe.id) return recipe;
    if (CF19_MANUAL_MORE_STEPS[recipe.id]) {
      return {...recipe, steps: CF19_MANUAL_MORE_STEPS[recipe.id]};
    }
    return recipe;
  }

  if (typeof normalizeRecipe === "function") {
    const oldNormalize = window.normalizeRecipe || normalizeRecipe;
    window.normalizeRecipe = function(r, i) {
      return withManualSteps(oldNormalize(r, i));
    };
  }

  if (Array.isArray(recipes) && recipes.length) {
    recipes = recipes.map(withManualSteps);
    allRecipes = [...recipes, ...(typeof getUserRecipes === "function" ? getUserRecipes() : [])];
    if (typeof renderAll === "function") renderAll();
    if (typeof updateFloatingRandom === "function") updateFloatingRandom();
  }

  if (document && document.body) {
    document.body.dataset.cozyVersion = window.CF19_VERSION;
  }
  console.info("Cozy Foodie v19 manual more steps loaded", Object.keys(CF19_MANUAL_MORE_STEPS).length);
})();

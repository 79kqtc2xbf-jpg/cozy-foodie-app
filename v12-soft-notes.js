window.stepMistake = function(step){
  let s = String(step || "").toLowerCase();
  if (s.includes("жар")) return "Ошибочка: слишком часто переворачивать. Дай корочке схватиться, потом уже спасай мир.";
  if (s.includes("вар")) return "Ошибочка: варить на максимуме всё время. Так еда разваливается и пытается сбежать из кастрюли.";
  if (s.includes("туш")) return "Ошибочка: забыть жидкость. Тушение любит немного влаги и спокойствие.";
  if (s.includes("сыр")) return "Ошибочка: кипятить сырный соус. Лучше тихий огонь и нежное помешивание.";
  return "Милое замечание: не надо пытаться идеально. Лучше вкусно и спокойно, чем красиво и с нервным тиком.";
};
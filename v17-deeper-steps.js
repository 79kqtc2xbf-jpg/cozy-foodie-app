function cf17MainStep(step, r, i) {
  const title = r.title || "это блюдо";
  const raw = String(step || "").trim();
  const action = typeof cf16Action === "function" ? cf16Action(raw) : "general";
  const type = typeof cf16DishType === "function" ? cf16DishType(r) : "home";
  const main = typeof cf16MainIngredient === "function" ? cf16MainIngredient(r) : "основу";
  const heat = r.heat || "средний огонь";
  const lower = raw.toLowerCase();
  const intro = {
    prep: `Подготовь ${main} для «${title}»: ${raw}`,
    boil: `Варочный этап для «${title}»: ${raw}`,
    fry: `Сковорода для «${title}»: ${raw}`,
    stew: `Тушение для «${title}»: ${raw}`,
    mix: `Сборка вкуса для «${title}»: ${raw}`,
    sauce: `Соусный этап для «${title}»: ${raw}`,
    wrap: `Сборка в лаваше для «${title}»: ${raw}`,
    finish: `Финальный штрих для «${title}»: ${raw}`,
    general: `${raw}`
  };
  let extra = "";
  if (type === "soup") extra = "Держи мягкое кипение: суп должен спокойно булькать, а не плеваться на плиту. После каждого нового ингредиента дай кастрюле снова прогреться.";
  else if (type === "pasta") extra = "Пасту лучше доводить до состояния, где она ещё держит форму. Если есть соус, финальные минуты она доберёт уже в нём.";
  else if (type === "potato") extra = "Картошка любит сухую поверхность и терпение. Если жаришь — не мешай каждую секунду, дай появиться корочке.";
  else if (type === "chicken") extra = "Курицу не передерживай: кусочки должны побелеть внутри и остаться сочными. Слишком долгий огонь делает её сухой и грустной.";
  else if (type === "rice") extra = "Рису важно не мешать бесконечно. Дай ему прогреться или дойти под крышкой, чтобы зерно осталось аккуратным.";
  else if (type === "buckwheat") extra = "Гречка становится вкуснее, если дать ей пару минут постоять под крышкой. Она впитает лишнюю влагу и станет рассыпчатее.";
  else if (type === "lavash") extra = "Начинки клади умеренно: лаваш должен закрыться без борьбы. Жарь швом вниз, чтобы он не раскрылся.";
  else if (type === "salad") extra = "Заправку добавляй постепенно и ближе к подаче. Так салат останется свежим, а не превратится в мокрый компромисс.";
  else if (type === "porridge") extra = "Кашу веди на слабом огне и мешай у дна. После выключения она ещё густеет, поэтому не вари до состояния бетона.";
  else if (type === "sweet") extra = "В сладких блюдах средний огонь — лучший друг. Снаружи они темнеют быстро, а внутри могут ещё не успеть приготовиться.";
  else if (type === "egg") extra = "Яйца любят спокойный огонь. Сними чуть раньше полной готовности — остаточное тепло доведёт их мягко.";
  else if (type === "meat") extra = "Мясной основе сначала дай схватиться, потом доводи спокойнее. Так будет вкуснее, чем если сразу всё тревожно мешать.";
  else if (type === "mushroom") extra = "Грибы сначала отдают влагу, и это нормально. Подожди, пока жидкость уйдёт, потом появится румяность и вкус.";
  else if (type === "fish") extra = "Рыба готовится быстро. Лучше проверить раньше, чем пересушить: она должна легко разделяться вилкой.";
  else extra = "Ориентируйся на запах, текстуру и спокойный огонь. Рецепт помогает, но твоя сковородка всегда имеет характер.";
  if (lower.includes("сыр") || lower.includes("слив") || lower.includes("сметан")) extra += " Молочное и сырное добавляй на умеренном огне, без активного кипения.";
  if (lower.includes("накрой") || lower.includes("крыш")) extra += " Под крышкой блюдо доходит мягче, но если снизу темнеет — сразу убавь огонь.";
  return `${intro[action] || raw} ${extra}`;
}

function cf17QualityCheck(r) {
  const type = typeof cf16DishType === "function" ? cf16DishType(r) : "home";
  const title = r.title || "блюдо";
  const checks = {
    soup: `Проверка готовности «${title}»: картофель/крупа мягкие, бульон не водянистый, соль чувствуется, но не спорит со всеми.`,
    pasta: `Проверка готовности «${title}»: паста не разваливается, соус обволакивает, а не лежит лужей отдельно.`,
    potato: `Проверка готовности «${title}»: картошка мягкая внутри, снаружи есть румяность или сливочная мягкость — по задумке блюда.`,
    chicken: `Проверка готовности «${title}»: курица белая внутри, сок прозрачный, кусочки не резиновые.`,
    rice: `Проверка готовности «${title}»: рис горячий, не сухой и не мокрый, зерно держит форму.`,
    lavash: `Проверка готовности «${title}»: лаваш держит форму, сыр расплавился, начинка не вываливается с видом “я ухожу”.`,
    salad: `Проверка готовности «${title}»: всё нарезано удобно, заправки достаточно, но салат не плавает.`,
    sweet: `Проверка готовности «${title}»: внутри мягко, снаружи не горчит, сладость не забивает вкус.`,
    egg: `Проверка готовности «${title}»: яйца схватились, но не стали резиновым ковриком.`,
    meat: `Проверка готовности «${title}»: мясо прожарено/протушено, но осталось сочным.`,
    fish: `Проверка готовности «${title}»: рыба легко разделяется вилкой и не сухая.`
  };
  return checks[type] || `Проверка готовности «${title}»: попробуй маленький кусочек и доведи соль, огонь и текстуру до состояния “да, это можно есть с удовольствием”.`;
}

function renderDetailedSteps(r) {
  const steps = (r.steps || []).map((st, i) => `
    <div class="step-card step-card-v17">
      <div class="step-num">${i + 1}</div>
      <div>
        <strong>${cf17MainStep(st, r, i)}</strong>
        <p>${typeof stepDetail === "function" ? stepDetail(st, r, i) : "Готовь спокойно и пробуй по ходу."}</p>
        <div class="trick">✨ ${typeof cf16Trick === "function" ? cf16Trick(st, r, i) : "Хитрость: держи огонь умеренным и не торопи блюдо."}</div>
        <div class="mistake">⚠️ ${typeof stepMistake === "function" ? stepMistake(st, r, i) : "Милое замечание: не пытайся идеально, делай вкусно."}</div>
      </div>
    </div>`).join("");
  return steps + `<div class="step-card final-check"><div class="step-num">✓</div><div><strong>${cf17QualityCheck(r)}</strong><p>Если вкус кажется пустым — добавь щепотку соли, немного кислоты или дай блюду постоять 2–5 минут. Это не магия, это финальная настройка.</p></div></div>`;
}
